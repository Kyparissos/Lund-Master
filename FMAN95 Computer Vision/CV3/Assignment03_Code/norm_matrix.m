function N = norm_matrix(x)
    m_ = mean(x(1:2,:),2);
    s_ = std(x(1:2,:),0,2);
%     N = [s_(1)       0         -(s_(1)*m_(1));
%            0        s_(2)      -(s_(2)*m_(2));
%            0         0               1      ];
    N = [1/s_(1) 0 0; 0 1/s_(2) 0; 0 0 1] * [1 0 -m_(1); 0 1 -m_(2); 0 0 1];

end
